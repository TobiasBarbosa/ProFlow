package org.example.proflow.model;

public enum Status {

    //***ENUMS***-------------------------------------------------------------------------------------------------------
    ACTIVE("Active"),
    INACTIVE("Inactive");

    //***ATTRIBUTES***--------------------------------------------------------------------------------------------------
    final String displayStatus;

    //***CONSTRUCTOR***-------------------------------------------------------------------------------------------------
    Status(String displayStatus) {
        this.displayStatus = displayStatus;
    }

    //***GETTER METHODS***----------------------------------------------------------------------------------------------
    public String getDisplayStatus() {
        return displayStatus;
    }


    //***END***---------------------------------------------------------------------------------------------------------
}
